package com.example.skill_it_2021_09.Data;

public class  IntPair{

    private int id;
    private int cnt;

    public IntPair(int id, int cnt) {
        this.id = id;
        this.cnt = cnt;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCnt() {
        return cnt;
    }
}
